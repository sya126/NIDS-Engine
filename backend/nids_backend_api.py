@app.route('/predict_file', methods=['POST', 'OPTIONS'])
def predict_file():
    if request.method == 'OPTIONS': return make_response()
    try:
        file = request.files['file']
        df_upload = pd.read_csv(file)
        df_upload.columns = df_upload.columns.astype(str).str.strip()
        existing_numeric = [c for c in numeric_cols if c in df_upload.columns]
        is_already_scaled = df_upload[existing_numeric].max().max() <= 1.0 if existing_numeric else False
        processed = preprocess_pipeline(df_upload, apply_scaling=not is_already_scaled)
        predictions = model.predict(processed)
        labels = [inverse_mapping[int(p)] for p in predictions]
        output = {'total': len(labels), 'attacks': sum(1 for L in labels if L != 'NORMAL'), 'details': pd.Series(labels).value_counts().to_dict(), 'status': 'success'}
        print(f"\n📁 DOSYA ANALİZİ: {output}\n")
        res = jsonify(output)
        res.headers.add("ngrok-skip-browser-warning", "true")
        return res
    except Exception as e: return jsonify({'status': 'error', 'message': str(e)}), 500


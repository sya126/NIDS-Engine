# Trafik yoğunluğunu ölçmek için basit bir sayaç
traffic_stats = {"packet_count": 0, "last_reset": time.time()}

def network_traffic_validator(packet, ml_prediction):
    """
    Modelin sonucunu ağın fiziksel gerçekliğiyle doğrular.
    """
    global traffic_stats
    current_time = time.time()
    packet_size = len(packet)

    # 1 saniyede bir sayacı sıfırla
    if current_time - traffic_stats["last_reset"] > 1.0:
        traffic_stats["packet_count"] = 0
        traffic_stats["last_reset"] = current_time

    traffic_stats["packet_count"] += 1

    # DOĞRULAMA MANTIĞI:
    if ml_prediction == 'DOS':
        if traffic_stats["packet_count"] < 50 and packet_size < 1500:
            return 'NORMAL'

    return ml_prediction

#core preprocessing
def preprocess_pipeline(raw_df, apply_scaling=True):
    full_df = pd.DataFrame(0, index=np.arange(len(raw_df)), columns=all_columns)
    for col in raw_df.columns:
        str_col = str(col)
        if str_col in full_df.columns:
            full_df[str_col] = raw_df[col]
    if apply_scaling:
        full_df[numeric_cols] = scaler.transform(full_df[numeric_cols])
    final_features = [str(f) for f in top_features]
    return full_df[final_features]

#real time sniffer
def real_time_sniffer():
    def process_packet(packet):
        if IP in packet:
            src_bytes = len(packet)
            is_http = 1.0 if packet.haslayer(TCP) and (packet[TCP].dport == 80 or packet[TCP].sport == 80) else 0.0
            is_ftp = 1.0 if packet.haslayer(TCP) and (packet[TCP].dport == 21 or packet[TCP].sport == 21) else 0.0

            live_payload = {"4": src_bytes, "5": 0.0, "32": 1.0, "34": 0.0, "11": 0.0, "37": 0.0, "2_http": is_http, "23": 0.01, "35": 0.0, "22": 0.01, "0": 0.0, "33": 1.0, "31": 1.0, "9": 0.0, "2_ftp_data": is_ftp, "29": 0.0, "36": 0.0, "3_S0": 0.0, "24": 0.0, "25": 0.0}

            df_incoming = pd.DataFrame([live_payload])
            processed = preprocess_pipeline(df_incoming, apply_scaling=True)
            prediction_id = int(model.predict(processed)[0])
            ml_result = inverse_mapping[prediction_id]

            final_result = network_traffic_validator(packet, ml_result)

            if final_result != 'NORMAL':
                print(f"🚨 ATAK TESPİTİ (Onaylandı): {final_result} - Hedef IP: {packet[IP].dst}")
                socketio.emit('live_threat_alert', {
                    'prediction': final_result,
                    'details': f"IP: {packet[IP].src} -> {packet[IP].dst} | Filtre: Ağ Analizcisi Onaylı"
                })

    print("📡 Sniffer Aktif: Hibrit Analiz Modu (ML + Ağ Kuralları)")
    sniff(filter="tcp or udp", prn=process_packet, store=False)

sniffer_thread = threading.Thread(target=real_time_sniffer, daemon=True)
sniffer_thread.start()

